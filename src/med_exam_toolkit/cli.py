    # åŠ è½½é¢˜åº“
    if bank:
        questions = load_bank(Path(bank), password)
    else:
        questions = load_json_files(input_dir, parser_map)
        if not questions:
            click.echo("é¢˜åº“ä¸ºç©ºã€‚")
            sys.exit(1)
        if dedup:
            questions = deduplicate(questions, "strict")

    click.echo(f"é¢˜åº“åŠ è½½å®Œæˆ: {len(questions)} é“é¢˜")

    # è§£æž per_mode
    mode_dist = {}
